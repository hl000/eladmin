package me.zhengjie.domain;

public class ProductInfo {

    private String fNumber;

    private Integer fId;

    private String fName;

    private Float fOutputquantity;

    private Float fRejectsquantity;

    private Float fBalancequantity;

    private String fNote;

    public String getfNumber(){ return fNumber;  }

    public void setfNumber(String fNumber){ this.fNumber = fNumber; }

    public String getfName(){ return fName;}

    public void setfName(String fName){ this.fName = fName;}

    public Float getfOutputquantity(){ return fOutputquantity;}

    public void setfOutputquantity(Float fOutputquantity){ this.fOutputquantity = fOutputquantity;}

    public Float getfRejectsquantity(){ return fRejectsquantity;}

    public void setfRejectsquantity(Float fRejectsquantity){ this.fRejectsquantity = fRejectsquantity;}

    public String getfNote(){ return fNote;}

    public void setfNote(String fNote){ this.fNote = fNote;}

    public Integer getfId(){ return fId;}

    public void setfId(Integer fId){ this.fId = fId;}

    public Float getfBalancequantity(){ return fBalancequantity;}

    public void setfBalancequantity(Float fBalancequantity){ this.fBalancequantity = fBalancequantity;}

}
